<?php

namespace App\Http\Controllers;

use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\DB;

class ExportController extends Controller
{
    public function exportCsv()
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI LOGIN & ROLE
        |--------------------------------------------------------------------------
        */
        if (!Auth::check() || Auth::user()->role !== 'hr') {
            abort(403, 'Akses ditolak. Hanya untuk HR.');
        }

        $perusahaan_id = Auth::id();

        /*
        |--------------------------------------------------------------------------
        | NAMA FILE
        |--------------------------------------------------------------------------
        */
        $filename = 'Data_Pelamar_' . date('Y-m-d_H-i-s') . '.csv';

        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA PELAMAR (FIX RELASI)
        |--------------------------------------------------------------------------
        | applications -> pelamars -> users
        |--------------------------------------------------------------------------
        */
        $pelamar = DB::table('applications as a')
            ->join('jobs as j', 'a.job_id', '=', 'j.id')
            ->join('pelamars as p', 'a.pelamar_id', '=', 'p.id')
            ->join('users as u', 'p.user_id', '=', 'u.id')
            ->select(

                /*
                |--------------------------------------------------------------------------
                | DATA USER (FIX: nama_lengkap)
                |--------------------------------------------------------------------------
                */
                'u.nama_lengkap',
                'u.email',

                /*
                |--------------------------------------------------------------------------
                | DATA PELAMAR
                |--------------------------------------------------------------------------
                */
                'p.no_hp',
                'p.pendidikan',
                'p.skills',

                /*
                |--------------------------------------------------------------------------
                | DATA LAMARAN
                |--------------------------------------------------------------------------
                */
                'a.status',
                'a.tanggal_lamar',

                /*
                |--------------------------------------------------------------------------
                | DATA JOB
                |--------------------------------------------------------------------------
                */
                'j.posisi',
                'j.lokasi',
                'j.tipe_pekerjaan'
            )

            /*
            |--------------------------------------------------------------------------
            | FILTER HR
            |--------------------------------------------------------------------------
            */
            ->where('j.perusahaan_id', $perusahaan_id)

            /*
            |--------------------------------------------------------------------------
            | SOFT DELETE SAFETY
            |--------------------------------------------------------------------------
            */
            ->whereNull('a.deleted_at')
            ->whereNull('j.deleted_at')

            /*
            |--------------------------------------------------------------------------
            | URUTAN TERBARU
            |--------------------------------------------------------------------------
            */
            ->orderBy('a.created_at', 'desc')
            ->get();

        /*
        |--------------------------------------------------------------------------
        | HEADER CSV
        |--------------------------------------------------------------------------
        */
        $headers = [
            'Content-Type' => 'text/csv; charset=UTF-8',
            'Content-Disposition' => "attachment; filename=\"{$filename}\"",
            'Pragma' => 'no-cache',
            'Cache-Control' => 'must-revalidate, post-check=0, pre-check=0',
            'Expires' => '0'
        ];

        /*
        |--------------------------------------------------------------------------
        | STREAM CSV
        |--------------------------------------------------------------------------
        */
        $callback = function () use ($pelamar) {

            $file = fopen('php://output', 'w');

            /*
            |--------------------------------------------------------------------------
            | UTF-8 BOM (Excel SAFE)
            |--------------------------------------------------------------------------
            */
            fprintf($file, chr(0xEF) . chr(0xBB) . chr(0xBF));

            /*
            |--------------------------------------------------------------------------
            | HEADER CSV
            |--------------------------------------------------------------------------
            */
            fputcsv($file, [
                'No',
                'Nama Pelamar',
                'Email',
                'No HP',
                'Pendidikan',
                'Posisi',
                'Lokasi',
                'Tipe Pekerjaan',
                'Status Lamaran',
                'Tanggal Melamar',
                'Skills'
            ]);

            /*
            |--------------------------------------------------------------------------
            | DATA CSV
            |--------------------------------------------------------------------------
            */
            $no = 1;

            foreach ($pelamar as $row) {

                fputcsv($file, [
                    $no++,
                    $row->nama_lengkap ?? '-',   // FIX HERE
                    $row->email ?? '-',
                    $row->no_hp ?? '-',
                    $row->pendidikan ?? '-',
                    $row->posisi ?? '-',
                    $row->lokasi ?? '-',
                    $row->tipe_pekerjaan ?? '-',
                    $row->status ?? '-',
                    $row->tanggal_lamar ?? '-',
                    $row->skills ?? '-'
                ]);
            }

            fclose($file);
        };

        /*
        |--------------------------------------------------------------------------
        | RETURN STREAM
        |--------------------------------------------------------------------------
        */
        return response()->stream($callback, 200, $headers);
    }
}
